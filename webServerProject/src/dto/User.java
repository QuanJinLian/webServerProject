package dto;

public class User {
	private String id;
	private String pwd;
	private String nikName;
	private String photo;
	private String indate;
	private String friendList;
	
	public String getId() {
		return id;
	}
	public User setId(String id) {
		this.id = id;
		return this;
	}
	public String getPwd() {
		return pwd;
	}
	public User setPwd(String pwd) {
		this.pwd = pwd;
		return this;
	}
	public String getNikName() {
		return nikName;
	}
	public User setNikName(String nikName) {
		this.nikName = nikName;
		return this;
	}
	public String getPhoto() {
		return photo;
	}
	public User setPhoto(String photo) {
		this.photo = photo;
		return this;
	}
	public String getIndate() {
		return indate;
	}
	public User setIndate(String indate) {
		this.indate = indate;
		return this;
	}
	public String getFriendList() {
		return friendList;
	}
	public User setFriendList(String friendList) {
		this.friendList = friendList;
		return this;
	}
	

}
