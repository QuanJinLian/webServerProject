package dto;

public class Image {
	private String image;
	private String filename;
	
	
	public String getImage() {
		return image;
	}
	public Image setImage(String image) {
		this.image = image;
		return this;
	}
	public String getFilename() {
		return filename;
	}
	public Image setFilename(String filename) {
		this.filename = filename;
		return this;
	}
	

}
